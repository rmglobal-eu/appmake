"use client";

import { useState } from "react";
import { signIn } from "next-auth/react";
import { useRouter } from "next/navigation";
import { useTranslations } from "next-intl";
import Image from "next/image";
import Link from "next/link";
import { toast } from "sonner";
import { Loader2 } from "lucide-react";
import { Button } from "@/components/ui/button";
import { Input } from "@/components/ui/input";
import { ThemeToggle } from "@/components/ThemeToggle";
import { LoginShowcase } from "@/components/auth/LoginShowcase";

interface AuthPageProps {
  mode: "login" | "register";
}

export function AuthPage({ mode }: AuthPageProps) {
  const isLogin = mode === "login";
  const router = useRouter();
  const t = useTranslations("auth");

  const [name, setName] = useState("");
  const [email, setEmail] = useState("");
  const [password, setPassword] = useState("");
  const [loading, setLoading] = useState(false);

  async function handleSubmit(e: React.FormEvent) {
    e.preventDefault();
    if (!email || !password) return;
    setLoading(true);

    try {
      if (isLogin) {
        const res = await signIn("credentials", {
          email,
          password,
          redirect: false,
        });
        if (res?.error) {
          toast.error(t("invalidEmailPassword"));
        } else {
          router.push("/dashboard");
        }
      } else {
        // Register
        const res = await fetch("/api/auth/register", {
          method: "POST",
          headers: { "Content-Type": "application/json" },
          body: JSON.stringify({ name, email, password }),
        });
        const data = await res.json();

        if (!res.ok) {
          toast.error(data.error || t("somethingWentWrong"));
        } else {
          // Auto-login after registration
          const loginRes = await signIn("credentials", {
            email,
            password,
            redirect: false,
          });
          if (loginRes?.error) {
            toast.error(t("accountCreated"));
            router.push("/login");
          } else {
            router.push("/dashboard");
          }
        }
      }
    } catch {
      toast.error(t("somethingWentWrong"));
    } finally {
      setLoading(false);
    }
  }

  return (
    <div className="flex h-screen">
      {/* ── Left: Auth panel ── */}
      <div className="relative flex w-full flex-col lg:w-[480px] lg:shrink-0 lg:border-r">
        {/* Top bar */}
        <div className="flex items-center justify-between p-6">
          <Link href="/" className="flex items-center gap-2">
            <Image
              src="/icon-dark.svg"
              alt="AppMake"
              width={24}
              height={24}
              className="hidden dark:block"
            />
            <Image
              src="/icon-light.svg"
              alt="AppMake"
              width={24}
              height={24}
              className="block dark:hidden"
            />
            <span className="text-sm font-semibold tracking-tight">
              AppMake
            </span>
          </Link>
          <ThemeToggle />
        </div>

        {/* Auth form */}
        <div className="flex flex-1 flex-col items-center justify-center px-8">
          <div className="w-full max-w-sm space-y-6">
            {/* Heading */}
            <div>
              <h1 className="text-2xl font-bold tracking-tight">
                {isLogin ? t("welcomeBack") : t("createAccount")}
              </h1>
              <p className="mt-1 text-sm text-muted-foreground">
                {isLogin
                  ? t("signInToContinue")
                  : t("startBuilding")}
              </p>
            </div>

            {/* Email/password form */}
            <form onSubmit={handleSubmit} className="space-y-3">
              {!isLogin && (
                <Input
                  type="text"
                  placeholder={t("name")}
                  value={name}
                  onChange={(e) => setName(e.target.value)}
                  autoComplete="name"
                />
              )}
              <Input
                type="email"
                placeholder={t("email")}
                value={email}
                onChange={(e) => setEmail(e.target.value)}
                required
                autoComplete="email"
              />
              <Input
                type="password"
                placeholder={t("password")}
                value={password}
                onChange={(e) => setPassword(e.target.value)}
                required
                minLength={6}
                autoComplete={isLogin ? "current-password" : "new-password"}
              />
              <Button
                type="submit"
                size="lg"
                className="w-full"
                disabled={loading}
              >
                {loading && <Loader2 className="mr-2 h-4 w-4 animate-spin" />}
                {isLogin ? t("signIn") : t("createAccountButton")}
              </Button>
            </form>

            {/* Divider */}
            <div className="relative">
              <div className="absolute inset-0 flex items-center">
                <div className="w-full border-t" />
              </div>
              <div className="relative flex justify-center text-xs">
                <span className="bg-background px-3 text-muted-foreground">
                  {t("orContinueWith")}
                </span>
              </div>
            </div>

            {/* OAuth buttons */}
            <div className="grid grid-cols-3 gap-3">
              <Button
                onClick={() =>
                  signIn("google", { callbackUrl: "/dashboard" })
                }
                variant="outline"
                size="lg"
                className="w-full"
              >
                <svg className="h-4 w-4" viewBox="0 0 24 24">
                  <path
                    d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92a5.06 5.06 0 0 1-2.2 3.32v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.1z"
                    fill="#4285F4"
                  />
                  <path
                    d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
                    fill="#34A853"
                  />
                  <path
                    d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
                    fill="#FBBC05"
                  />
                  <path
                    d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
                    fill="#EA4335"
                  />
                </svg>
              </Button>

              <Button
                onClick={() =>
                  signIn("github", { callbackUrl: "/dashboard" })
                }
                variant="outline"
                size="lg"
                className="w-full"
              >
                <svg
                  className="h-4 w-4"
                  viewBox="0 0 24 24"
                  fill="currentColor"
                >
                  <path d="M12 2C6.477 2 2 6.484 2 12.017c0 4.425 2.865 8.18 6.839 9.504.5.092.682-.217.682-.483 0-.237-.008-.868-.013-1.703-2.782.605-3.369-1.343-3.369-1.343-.454-1.158-1.11-1.466-1.11-1.466-.908-.62.069-.608.069-.608 1.003.07 1.531 1.032 1.531 1.032.892 1.53 2.341 1.088 2.91.832.092-.647.35-1.088.636-1.338-2.22-.253-4.555-1.113-4.555-4.951 0-1.093.39-1.988 1.029-2.688-.103-.253-.446-1.272.098-2.65 0 0 .84-.27 2.75 1.026A9.564 9.564 0 0 1 12 6.844a9.59 9.59 0 0 1 2.504.337c1.909-1.296 2.747-1.027 2.747-1.027.546 1.379.202 2.398.1 2.651.64.7 1.028 1.595 1.028 2.688 0 3.848-2.339 4.695-4.566 4.943.359.309.678.92.678 1.855 0 1.338-.012 2.419-.012 2.747 0 .268.18.58.688.482A10.02 10.02 0 0 0 22 12.017C22 6.484 17.522 2 12 2z" />
                </svg>
              </Button>

              <Button
                onClick={() =>
                  signIn("microsoft-entra-id", {
                    callbackUrl: "/dashboard",
                  })
                }
                variant="outline"
                size="lg"
                className="w-full"
              >
                <svg className="h-4 w-4" viewBox="0 0 23 23">
                  <rect x="1" y="1" width="10" height="10" fill="#F25022" />
                  <rect x="12" y="1" width="10" height="10" fill="#7FBA00" />
                  <rect x="1" y="12" width="10" height="10" fill="#00A4EF" />
                  <rect x="12" y="12" width="10" height="10" fill="#FFB900" />
                </svg>
              </Button>
            </div>

            {/* Dev login (login only) */}
            {isLogin && (
              <Button
                onClick={() =>
                  signIn("credentials", {
                    email: "dev@appmake.dk",
                    callbackUrl: "/dashboard",
                  })
                }
                variant="ghost"
                size="sm"
                className="w-full text-xs text-muted-foreground"
              >
                {t("devLogin")}
              </Button>
            )}
          </div>

          {/* Toggle link */}
          <p className="mt-8 text-sm text-muted-foreground">
            {isLogin ? (
              <>
                {t("dontHaveAccount")}{" "}
                <Link
                  href="/register"
                  className="font-medium text-foreground underline-offset-4 hover:underline"
                >
                  {t("signUp")}
                </Link>
              </>
            ) : (
              <>
                {t("alreadyHaveAccount")}{" "}
                <Link
                  href="/login"
                  className="font-medium text-foreground underline-offset-4 hover:underline"
                >
                  {t("signIn")}
                </Link>
              </>
            )}
          </p>
        </div>

        {/* Bottom */}
        <div className="p-6 text-center text-xs text-muted-foreground">
          {isLogin ? t("bySigningIn") : t("bySigningUp")}
        </div>
      </div>

      {/* ── Right: Animated showcase ── */}
      <div className="relative hidden flex-1 overflow-hidden lg:flex">
        <LoginShowcase />
      </div>
    </div>
  );
}
